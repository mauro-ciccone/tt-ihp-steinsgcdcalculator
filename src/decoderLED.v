module decoderLED (
    input [3:0] a, output reg [7:0] y
);

    always @(*) begin
        case (a)
            4'd0 : y = 8'b00111111; 
            4'd1 : y = 8'b00000110;
            4'd2 : y = 8'b01011011;
            4'd3 : y = 8'b01001111;
            4'd4 : y = 8'b01100110;
            4'd5 : y = 8'b01101101;
            4'd6 : y = 8'b01111101;
            4'd7 : y = 8'b00000111;
            4'd8 : y = 8'b01111111;
            4'd9 : y = 8'b01101111;
            4'd10 : y = 8'b01011111;
            4'd11 : y = 8'b01111100;
            4'd12 : y = 8'b00111001;
            4'd13 : y = 8'b01011110;
            4'd14 : y = 8'b01111001;
            4'd15 : y = 8'b01110001;
            default: y = 8'b01000000;
        endcase
    end

endmodule